<h3 class="mt20">About Phil</h3>
<p class="mt20">
    Phil’s career started with almost constantly breaking his family’s only PC back in 1998 to try and figure out how it worked. Since then he has been interested in tech and eventually began making websites as a teenager for his brother’s clients. When he turned 18, he started his own company and began providing web services and learning about entrepreneurship.
</p>
<p>
    He and his wife Lauren own multiple businesses including one of the largest multi-speciality and distinctly hospitality-forward dental offices in the Pennsylvania town he grew up in. They employ over 50 team members across their businesses and he enjoys growing and training his teams to meet the needs of clients and patients alike.
</p>
<p>
    Phil is able to bring a unique perspective to his web clients due to his background in operating brick and mortar businesses as well as his ability to understand complex business systems. He is able to not only deliver results that clients request but regularly contributes to business growth strategy and process development for his clients.
</p>
<p>
    Phil has served on several boards and is currently a member of the Business Advisory Board for Northampton Community College, which he has served on since 2016. In 2024, just six years after starting the dental practice, he was invited to speak at a marketing conference hosted by one of the largest dental marketing companies in the world and discussed how attendees could grow more productive and successful dental practices.
</p>
<p>
    Phil’s favorite hobbies include world travel with his wife, making craft cocktails, and he has been training in Brazilian Jiu Jitsu since 2012.
</p>
